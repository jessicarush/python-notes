# Escape Characters

# \ allows you to escape characters or run an escape sequence
# for example, escape the single-quote inside the string:

print('I am 6\'2" tall.')    

# using triple quotes also works """

# \\ = backslash
# \' = single quote
# \" = double quote
# \a = ASCII bell
# \b = ASCII backspace
# \f = ASCII formfeed
# \n = ASCII line break
# \r = ASCII full return
# \t = ASCII tab
# \v = ASCII vertical tab
# \N{name} = unicode character name in the unicode database
# \uxxxx = unicode character with 16-bit hex value xxxx
# \Uxxxxxxxx = unicode character with 32-bit hex value xxxxxxxx
# \ooo = character with octal value ooo
# \xhh... = character wuth hex value hh..
